namespace DevInSales.Services
{
    public static class CalculatorService
    {

        public static int Soma(int x, int y)
        {
            return x + y;
        }

        public static object Subtrair(int x, int y)
        {
            return x - y;
        }

        public static object Multiplicar(int x, int y)
        {
            return x * y;
        }
    }
}
